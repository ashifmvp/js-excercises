function last(array, number) {
  if (number==null){
    return array[array.length-1];
  }
  
  else { 
    return array.slice(-number);}
}

console.log(last([7, 9, 0, -2]));
console.log(last([7, 9, 0, -2],3));
console.log(last([7, 9, 0, -2],6));


